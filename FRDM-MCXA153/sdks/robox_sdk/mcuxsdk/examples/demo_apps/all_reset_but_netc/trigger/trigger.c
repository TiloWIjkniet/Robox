int main(void)
{

	/* Init board hardware. */
 	BOARD_InitHardware();

    PRINTF("\r\nThe demo to trigger all_reset_but_netc reset starts.\r\n");

    while (1)
    {
        char ch;

        PRINTF("\r\nPress S to send all_reset_but_netc reset request.\r\n");
        ch = GETCHAR();
        if (ch == 'S' || ch == 's')
        {
            APP_GRP_RESET();
        }
    }
}
